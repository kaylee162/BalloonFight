void drawSpritesLevel1(void) {
    int oamIndex = 0;
    int i;

    // Player
    drawPlayerSprite(player.x - level1HOff, player.y - level1VOff);
    oamIndex = 1;

    // Enemies
    for (i = 0; i < MAX_ENEMIES; i++) {
        if (enemies[i].active) {
            drawEnemySprite(i, oamIndex, enemies[i].x - level1HOff, enemies[i].y - level1VOff);            oamIndex++;
        }
    }

    // Player bullets
    for (i = 0; i < MAX_PLAYER_BULLETS; i++) {
        if (playerBullets[i].active) {
            drawBulletSprite(oamIndex, playerBullets[i].x - level1HOff, playerBullets[i].y - level1VOff, 0);
            oamIndex++;
        }
    }

    // Enemy bullets
    for (i = 0; i < MAX_ENEMY_BULLETS; i++) {
        if (enemyBullets[i].active) {
            drawBulletSprite(oamIndex, enemyBullets[i].x - level1HOff, enemyBullets[i].y - level1VOff, 1);
            oamIndex++;
        }
    }

    // Balloons
    for (i = 0; i < MAX_BALLOONS; i++) {
        if (balloons[i].active) {
            drawBalloonSprite(oamIndex, balloons[i].x - level1HOff, balloons[i].y - level1VOff, balloons[i].spriteVariant);
            oamIndex++;
        }
    }

    // Door after all enemies are defeated
    if (doorVisible) {
        drawDoorSprite(doorX - level1HOff, doorY - level1VOff);
        oamIndex++;
    }

    // Hide all remaining sprites
    hideUnusedSpritesFrom(oamIndex);

    // Copy shadow OAM to hardware OAM
    DMANow(3, shadowOAM, OAM, 512);
}