void updateLevel1(void) {
    if (BUTTON_PRESSED(BUTTON_START)) {
        pausedState = STATE_LEVEL1;
        state = STATE_PAUSE;
        menuNeedsRedraw = 1;
        return;
    }

    if (player.invincibleTimer > 0) {
        player.invincibleTimer--;
    }

    updatePlayerLevel1();
    updatePlayerBullets();
    updateEnemyBullets();
    updateEnemies();
    updateBalloonsLevel1();
    updatePlayerAnimation();

    level1HOff = CLAMP(player.x - (SCREENWIDTH / 2) + (player.width / 2), 0, LEVEL1_PIXEL_W - SCREENWIDTH);
    level1VOff = CLAMP(player.y - (SCREENHEIGHT / 2), 0, LEVEL1_PIXEL_H - SCREENHEIGHT);
    setBackgroundOffset(1, level1HOff, level1VOff);

    if (enemiesRemaining <= 0 && !doorVisible) {
        doorVisible = 1;
        sfxDoorAppear();
    }

    if (doorVisible) {
        // Door is 32x32 pixels (4 tiles wide x 4 tiles tall)
        if (collision(player.x, player.y, player.width, player.height,
                      doorX, doorY, 32, 32)) {
            if (!doorOpen) {
                // First contact: open the door
                doorOpen = 1;
                sfxDoorOpen();
            } else {
                // Second contact while open: enter and advance to level 2
                sfxDoorEnter();
                state = STATE_LEVEL2_INTRO;
                menuNeedsRedraw = 1;
                return;
            }
        }
    }

    if (player.lives <= 0) {
        state = STATE_LOSE;
        menuNeedsRedraw = 1;
    }
}

void updatePlayerLevel1(void) {
    int nextX;
    int floatStrength;

    // Save old position
    player.oldX = player.x;
    player.oldY = player.y;

    // Default movement state
    player.isMoving = 0;

    // Horizontal movement
    // Move left with a small step-up assist for bridge/platform edges.
    if (BUTTON_HELD(BUTTON_LEFT)) {
        int i;
        int steppedUp = 0;
        for (i = 0; i < 2; i++) {
            nextX = player.x - 1;

            if (canMoveTo(nextX, player.y, player.width, player.height)) {
                player.x = nextX;
            } else if (player.grounded && !steppedUp &&
                       canMoveTo(nextX, player.y - 1, player.width, player.height)) {
                // Only step up once per frame and only when grounded on a platform edge.
                player.x = nextX;
                player.y -= 1;
                steppedUp = 1;
            } else {
                break;
            }
        }

        player.direction = DIR_LEFT;
        player.isMoving = 1;
    }

    if (BUTTON_HELD(BUTTON_RIGHT)) {
        int i;
        int steppedUp = 0;
        for (i = 0; i < 2; i++) {
            nextX = player.x + 1;

            if (canMoveTo(nextX, player.y, player.width, player.height)) {
                player.x = nextX;
            } else if (player.grounded && !steppedUp &&
                       canMoveTo(nextX, player.y - 1, player.width, player.height)) {
                // Only step up once per frame and only when grounded on a platform edge.
                player.x = nextX;
                player.y -= 1;
                steppedUp = 1;
            } else {
                break;
            }
        }

        player.direction = DIR_RIGHT;
        player.isMoving = 1;
    }

    // Check whether the player is standing on solid ground
    if (!canMoveTo(player.x, player.y + 1, player.width, player.height)) {
        player.grounded = 1;
    } else {
        player.grounded = 0;
    }

    // Press A to float/jump upward.
    //
    // Each press gives an upward impulse based on remaining lives.
    // More lives = stronger float height.
    //
    // This still works both on the ground and in the air, so the player can
    // float upward repeatedly as long as they still have lives remaining.
    if (BUTTON_PRESSED(BUTTON_UP) && player.lives > 0) {
        player.yVel = getFloatVelocityForLives(player.lives);

        // Give the jump/float a short multi-frame boost window so the motion
        // feels more curved and natural instead of looking like a single-frame
        // pop upward.
        player.floatBoostTimer = player.lives + 1;

        player.grounded = 0;
        player.isMoving = 1;
    }

    // A held = fly upward.
    //
    // This is more reliable than a one-frame BUTTON_PRESSED impulse and matches
    // the intended "float/fly upward" behavior much better.
    //
    // More lives = stronger upward speed:
    // 3 lives -> fastest upward
    // 2 lives -> medium upward
    // 1 life  -> weakest upward
    //
    // This works both from the ground and in the air.
    if (BUTTON_HELD(BUTTON_UP) && player.lives > 0) {
        int targetUpVel = getFloatVelocityForLives(player.lives);

        // Start flying if grounded.
        player.grounded = 0;
        player.isMoving = 1;

        // While A is held, keep pulling velocity upward toward the target.
        // This makes the movement feel smooth and controllable instead of
        // acting like a tiny single-frame hop.
        if (player.yVel > targetUpVel) {
            player.yVel--;
        }

        // Clamp so we do not fly upward faster than intended for this
        // number of lives.
        if (player.yVel < targetUpVel) {
            player.yVel = targetUpVel;
        }
    } else {
        // When A is not being held, gravity takes over normally.
        if (!player.grounded) {
            player.yVel += PLAYER_GRAVITY;

            if (player.yVel > PLAYER_MAX_FALL_SPEED) {
                player.yVel = PLAYER_MAX_FALL_SPEED;
            }
        }
    }

    // If grounded, do not keep downward falling speed.
    if (player.grounded && player.yVel > 0) {
        player.yVel = 0;
    }

    // This older boost timer behavior is no longer needed because flying is
    // now driven directly by holding A.
    player.floatBoostTimer = 0;

    // Apply vertical velocity one pixel at a time for smoother collision handling
    while (player.yVel < 0) {
        if (canMoveTo(player.x, player.y - 1, player.width, player.height)) {
            player.y--;
            player.yVel++;
        } else {
            player.yVel = 0;
            break;
        }
    }

    while (player.yVel > 0) {
        if (canMoveTo(player.x, player.y + 1, player.width, player.height)) {
            player.y++;
            player.yVel--;
        } else {
            player.yVel = 0;
            break;
        }
    }

    // Recompute grounded state after vertical movement
    if (!canMoveTo(player.x, player.y + 1, player.width, player.height)) {
        player.grounded = 1;
        if (player.yVel > 0) {
            player.yVel = 0;
        }
    } else {
        player.grounded = 0;
    }

    // Keep player inside the map bounds
    player.x = CLAMP(player.x, 0, LEVEL1_PIXEL_W - player.width);
    player.y = CLAMP(player.y, 0, LEVEL1_PIXEL_H - player.height);

    // Hazard check
    if (rectTouchesColor(player.x, player.y, player.width, player.height, CM_KILL)) {
        damagePlayer();
    }

    // Fire with B
    if (BUTTON_PRESSED(BUTTON_B)) {
        firePlayerBullet();
    }

    // Floating looks nicer if the player animation is treated as moving when drifting
    floatStrength = absInt(player.y - player.oldY);
    if (floatStrength > 0) {
        player.isMoving = 1;
    }
}

void updateBalloonsLevel1(void) {
    int i;

    for (i = 0; i < MAX_BALLOONS; i++) {
        if (!balloons[i].active) {
            continue;
        }

        if (collision(player.x, player.y, player.width, player.height,
                      balloons[i].x, balloons[i].y, balloons[i].width, balloons[i].height)) {

            balloons[i].active = 0;

            // Collecting a balloon always awards points — same behaviour whether
            // or not the player also gains a life.
            score += SCORE_BALLOON_BONUS;

            if (player.lives < 3) {
                player.lives++;
            }

            sfxBalloon();
        }
    }
}

